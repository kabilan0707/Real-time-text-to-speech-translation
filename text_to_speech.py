from googletrans import Translator
from gtts import gTTS
import os

# Initialize translator
translator = Translator()

# Input sentence (English or any language)
text = input("Enter a sentence: ")

# Target language
language = input("Enter language code (hi/ml/te/ta): ")

# Translate text
translated = translator.translate(text, dest=language)

# Output translated text
print("\nðŸ“„ Translated Text:")
print(translated.text)

# Convert translated text to speech
tts = gTTS(text=translated.text, lang=language)
tts.save("speech.mp3")

# Play audio
os.system("start speech.mp3")   # Windows
# os.system("afplay speech.mp3") # macOS
# os.system("mpg321 speech.mp3") # Linux

